export class PaddingSidesInsideBreakpoints extends CSSProperty<
  Map<Breakpoints, Map<SideMapping, Map<FlexSpacing, string>>>
> {
  private constructor(
    classesMapping: ClassesMapping<
      Map<Breakpoints, Map<SideMapping, Map<FlexSpacing, string>>>
    >,
    cssSelector: CSSSelector
  ) {
    super(classesMapping, cssSelector);
  }

  static create(selector: CSSSelector = "none") {
    const classesMapping = new PaddingClassesMapping();
    return new PaddingSidesInsideBreakpoints(classesMapping, selector);
  }

  public write(
    breakpoint: Breakpoints,
    side: SideMapping,
    spacing: FlexSpacing
  ) {
    return this.getClassName(breakpoint, side, spacing);
  }
}

