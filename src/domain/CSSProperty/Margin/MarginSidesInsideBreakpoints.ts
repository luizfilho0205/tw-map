export class MarginSidesInsideBreakpoints extends CSSProperty<
  Map<Breakpoints, Map<SideMapping, Map<MarginSpacing, string>>>
> {
  private constructor(
    classesMapping: ClassesMapping<
      Map<Breakpoints, Map<SideMapping, Map<MarginSpacing, string>>>
    >,
    cssSelector: CSSSelector
  ) {
    super(classesMapping, cssSelector);
  }

  static create(selector: CSSSelector = "none") {
    const classesMapping = new MarginClassesMapping();
    return new MarginSidesInsideBreakpoints(classesMapping, selector);
  }

  public write(
    breakpoint: Breakpoints,
    side: SideMapping,
    spacing: MarginSpacing
  ) {
    return this.getClassName(breakpoint, side, spacing);
  }
}

